@pytest.mark.development_environment
@pytest.mark.acceptance_environment
def test_openapi_schema(udm_rest_api, ldap):
    response = requests.get(
        f"{udm_rest_api.base_url}openapi.json",
        headers={"Accept": "application/json"},
        auth=HTTPBasicAuth(ldap.admin_rdn, ldap.admin_password),
    )
    openapi_schema = response.json()
    assert openapi_schema

    _with_identifier = []
    _without_identifier = []

    for component, schema in openapi_schema["components"]["schemas"].items():
        props = schema.get("properties", {}).get("properties", {}).get("properties", {})
        if not props:
            _without_identifier.append(component)
            continue
        if "univentionObjectIdentifier" in props.keys():
            _with_identifier.append(component)
            continue

    with_identifier = set(_with_identifier)
    without_identifier = set(_without_identifier)

    # Comparison with expected UDM modules ignoring custom modules like e.g. the OX Modules
    assert len(EXPECTED_WITH_IDENTIFIER - with_identifier) == 0
    assert len(EXPECTED_WITHOUT_IDENTIFIER - without_identifier) == 0


